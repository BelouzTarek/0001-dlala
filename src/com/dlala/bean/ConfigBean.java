package com.dlala.bean;

public class ConfigBean {
	
	String keyBean, view;
	boolean dispache;
	Object bean, classe;
	public String getKeyBean() {
		return keyBean;
	}
	public void setKeyBean(String keyBean) {
		this.keyBean = keyBean;
	}
	public String getView() {
		return view;
	}
	public void setView(String view) {
		this.view = view;
	}
	public boolean isDispache() {
		return dispache;
	}
	public void setDispache(boolean dispache) {
		this.dispache = dispache;
	}
	public Object getBean() {
		return bean;
	}
	public void setBean(Object bean) {
		this.bean = bean;
	}
	public Object getClasse() {
		return classe;
	}
	public void setClasse(Object classe) {
		this.classe = classe;
	}
	
	

}
